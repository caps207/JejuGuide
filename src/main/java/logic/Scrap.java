package logic;

public class Scrap {

	private String id;
	private String url;
	private String title;
	private int type;

	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	@Override
	public String toString() {
		return "Scrap [id=" + id + ", url=" + url + ", title=" + title + ", type=" + type + "]";
	}



}
